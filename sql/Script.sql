USE sys;

CREATE TABLE employees (
  id INT PRIMARY KEY AUTO_INCREMENT,
  name VARCHAR(100),
  position VARCHAR(50),
  salary DECIMAL(10,2),
  hire_date DATE
);

CREATE TABLE products (
  id INT PRIMARY KEY AUTO_INCREMENT,
  name VARCHAR(100),
  category VARCHAR(50),
  price DECIMAL(10,2)
);

CREATE TABLE sales (
  id INT PRIMARY KEY AUTO_INCREMENT,
  employee_id INT,
  product_id INT,
  quantity INT,
  sale_date DATE,
  FOREIGN KEY (employee_id) REFERENCES employees(id),
  FOREIGN KEY (product_id) REFERENCES products(id)
);



